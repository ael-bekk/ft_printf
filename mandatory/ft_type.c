#include "libftprintf.h"

int	ft_type(char c)
{
	return (1 * (c == 'c') + 2 * (c == 's') + 3 * (c == 'p')
		+ 4 * (c == 'd') + 5 * (c == 'i') + 6 * (c == 'u')
		+ 7 * (c == 'x') + 8 * (c == 'X') + 9 * (c == '%'));
}
